class Ex3_3 {
	public static void main(String args[]) {
		int i=5, j=5;
		System.out.println(i++);  // i의 값을 출력 후, 1 증가
		System.out.println(++j);  // j의 값을 1 증가 후, 출력
		System.out.println("i = " + i + ", j = " +j);
		
		System.out.println("-----------------");
		
		int x=5, d=5;
		System.out.println(x++);
		System.out.println(++d);
		System.out.println("x = " + x + ", d = "+ d);
	}
}